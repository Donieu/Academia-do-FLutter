//Um future é usado para representar um valor ou um erro que estará presente em algum momento no Futuro

//Um Stream é a mesma coisa, em um momento futuro você receberá um valor ou um erro. Porém a diferençan no future você receberá um unico resultado (sucesso ou erro), já o Stream é uma combinação de futures, dessa forma com Streans você pode receber vários valores (como um canal aberto)